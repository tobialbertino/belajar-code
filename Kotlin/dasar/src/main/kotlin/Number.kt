fun main() {
    var age : Byte = 30
    var number : Int = 200

    println(age)
    println(number)
    //    Float
    var sample : Float = 10.10F
    println(sample)

    var binary: Int = 0b010101
    println(binary)

    var price: Long =9_000_000_000L
    println(price)

    var priceInt: Int = price.toInt()
    println(priceInt)

    var doubleBinary: Double = binary.toDouble()
    println(doubleBinary)
}
