f <- function(x) {
  (x^5) - (4 * x^3) + 4
}

a0 <- 0.5
f(a0)
a1 <- 1.23
f(a1)
a2 <- 2
f(a2)

f01 <- (f(a1) - f(a0)) / (a1 - a0)
f01
f12 <- (f(a2) - f(a1)) / (a2 - a1)
f12

f123 <- (f12 - f01) / (a2 - a0)
f123

da <- (f123 * (a0 + a1) - f01) / (2 * f123)
da

